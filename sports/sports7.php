                        the Ynares Sports Arena.<br><br>

                        Game time is at 3:00 p.m., with the winner advancing to the best-of-3 championship series
                        against reigning title-holder EcoOil-La Salle. The finals start next week.<br><br>

                        San Beda forced a do-or-die Game 3 after clobbering Letran, 74-51, in Game 2 last Tuesday. But
                        Red Lions coach Yuri Escueta was quick to move on from that win.<br><br>

                        "I'm sure, coach Rensy (Bajar) will adjust. It's a do-or-die Game 3. They'll be ready and we
                        have to be ready for it. We have to show much more effort than Letran," said San Beda coach Yuri
                        Escueta.<br><br>

                        Behind a balanced attack led by Peter Alfaro and Jacob Cortez, San Beda practically dominated
                        Game 2 to more than atone for its disappointing meltdown in the series opener. <br><br>

                        The Red Lions blew an early 24-point lead in Game 1 and went on to surrender the match to the
                        Knights, 93-87, in overtime.

                        The Red Lions, the elims topnotcher, are not expecting Game 3 to be a walk in the park. Instead
                        they are prepared for a tough grind against the three-time reigning NCAA champion
                        Knights.<br><br>

                        "It's a test of character for us. We know we can bounce back as long as we play within our
                        system. It will be an exciting game," said neophyte mentor Bajar, who's counting on top gun Kurt
                        Reyson to regain his bearing after managing just eight points in Game 2.<br><br>

                        "Hindi namin napantayan 'yung energy nila sa Game 2. Hopefully, we can do some adjustment,
                        especially on defense."
