                    <p>
                        Jocel Lyn Ninobla gained prominence as poomsae queen in the region after a third consecutive
                        gold medal in the Southeast Asian (SEA) Games last month in Cambodia.<br><br>

                        Tapping her for the lone women’s individual gold medal that will be staked in the 19th Asian
                        Games (Asiad) in Hangzhou, China, in September is a no-brainer.<br><br>

                        “It’s a different level, but I’ll be up for the challenge,’’ said the 26-year-old Ninobla, whose
                        hat trick in the SEA Games came in the women’s team with rookies Aidaine Laxa and Nicole
                        Labayne.<br><br>

                        Ninobla wasn’t a contender in the 2018 Asian Games where the sports science degree holder from
                        University of Santo Tomas wasn’t able to progress to the quarterfinals.<br><br>

                        Indonesia’s Defia Rosmaniar is the defending Asian Games champion. But Ninobla has a great track
                        record against the Indonesian, having beaten her in 2019 Manila and 2021 Vietnam SEA
                        Games.<br><br>

                        “I just have to train doubly hard and work on my routine,’’ said Ninobla.<br><br>

                        Poomsae in the Asiad scheduled Sept. 23 to Oct. 8 will be much harder since organizers decided
                        to combine two events—recognized and freestyle poomsae—for a single medal.<br><br>

                        Ninobla, who raked in medals in the world and continental online meets during the pandemic, is a
                        recognized poomsae artist and should combine her expertise with the acrobatics and music of a
                        freestyle act.<br><br>

                        In the men’s individual, SEA Games gold medalist Patrick Perez and Kobe Macario will be battling
                        it out for the lone PH slot.<br><br>

                        Filipino jins garnered six gold medals in the SEA Games, including four in “kyorugi” where the
                        gold medalists are assured of spots in the Asian Games.<br><br>

                        Tokyo Olympian Kurt Barbosa (54 kilograms), Arven Alcantara (68 kg), Samuel Morrison (87 kg) and
                        Rio de Janeiro Olympian Kirstie Alora, all of whom captured gold medals in Cambodia, will banner
                        the team in the Asian Games.
