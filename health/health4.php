                    <p>
                        US Surgeon General Vivek Murthy says that while social media offers some benefits, there are
                        'ample indicators that social media could also harm children's well-being' <br><br>

                        Social media can profoundly harm the mental health of youth, particularly adolescent girls, the
                        US Surgeon General warned in an advisory on Tuesday, May 23, and he called for safeguards from
                        tech companies for children who are at critical stages of brain development.<br><br>

                        US Surgeon General Vivek Murthy said that while social media offers some benefits, there are
                        “ample indicators that social media could also harm children’s well-being.”<br><br>

                        “We are in the middle of a national youth mental health crisis, and I am concerned that social
                        media is an important driver of that crisis – one that we must urgently address,” Murthy
                        said.<br><br>

                        Social media use may cause and perpetuate body image issues, affect eating behaviors and sleep
                        quality, and lead to social comparison and low self-esteem, especially among adolescent girls,
                        the advisory said, citing responses from a survey conducted among adolescents.<br><br>

                        Adolescents who spend more than three hours per day on social media face double the risk of
                        experiencing poor mental health outcomes, such as symptoms of depression and anxiety, according
                        to the advisory.<br><br>

                        Most adolescents say social media helps them feel more accepted, more supported during tough
                        times, more connected to their friends, and more creative, the advisory also said.<br><br>

                        It said policymakers should strengthen safety standards in ways that enhance those benefits for
                        children of all ages, while noting that inappropriate and harmful content continues to be easily
                        and widely accessible to children.<br><br>

                        Tech companies should adhere to age limits to control access to social media platforms, and be
                        transparent about data regarding the impact of their products on children, the advisory
                        urges.<br><br>

                        Algorithms and platform design should seek to maximize the potential benefits of social media
                        instead of features designed to make users spend more time on them, it said.<br><br>

                        “The first principle of healthcare is to do no harm – that’s the same standard we need to start
                        holding social media platforms to,” said Saul Levin, CEO of the American Psychiatric
                        Association.<br><br>

                        The report includes suggestions for what parents, tech companies, as well as children and
                        adolescents, can do to avoid dangerous pitfalls and make the social media experience more
                        positive. They include creation of a family media plan, encouraging of in-person friendships,
                        talking to children about how they spend their time online, and encouraging them to seek help
                        should they need it.<br><br>

                        It included a reminder of the new 988 Suicide and Crisis Lifeline “if you or someone you know is
                        experiencing a mental health crisis.”

