                    <h1 class='l-heading'>Wegovy, other weight loss drugs ‘no silver bullet,’ says WHO amid obesity
                        review
                    </h1>
                    <div class="meta">
                        <small>
                            <i class="fas fa-user"></i>Written by <b>PJNews</b> - May 12, 2023
                        </small>
                        <div class="category category-health">Health</div>
