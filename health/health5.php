                    <p>
                        The global health body is first revising guidelines for treating children and adolescents with
                        obesity, and will then update recommendations for adults, says Francesco Branca, WHO director of
                        nutrition and food safety<br><br>

                        New highly-effective weight loss drugs such as Novo Nordisk’s Wegovy are not a “silver bullet”
                        for addressing the rapid rise in global obesity rates, the World Health Organization’s nutrition
                        chief told Reuters, as the agency conducts its first review of obesity management guidelines in
                        more than 20 years.<br><br>

                        The global health body is first revising guidelines for treating children and adolescents with
                        obesity, and will then update recommendations for adults, said Francesco Branca, WHO director of
                        nutrition and food safety.<br><br>

                        The WHO last issued global guidelines on the topic in 2000, which are used as a blueprint for
                        countries without the resources to draft their own plans.<br><br>

                        As part of the work, the WHO has commissioned the Mario Negri Institute for Pharmacological
                        Research, in Milan, Italy, to assess the evidence for the use of all drugs for children and
                        adolescents – from older options like GSK’s Xenical to newer, more effective treatments like
                        Wegovy and Eli Lilly and Co’s Mounjaro, Branca told Reuters.<br><br>

                        “The kind of communication that has been done around these drugs – ‘We’ve found a solution’ –
                        that’s wrong,” said Branca. Drugs for obesity are important but must be “part of a comprehensive
                        approach,” he said. “This is not a silver bullet.”<br><br>

                        Branca said that other interventions, including diet and exercise, remain critical to help
                        manage obesity. The latest WHO data shows that the percentage of children and adolescents aged 5
                        to 19 who are obese or overweight has risen to just over 18% in 2016 from 4% in 1975, and this
                        now represents more than 340 million people.<br><br>

                        Novo Nordisk and Eli Lilly did not immediately respond to a Reuters request for comment.<br><br>

                        Wegovy and Mounjaro were originally developed for type 2 diabetes to help control blood glucose.
                        More recently, they have been shown to help people lose around 15% of their body weight,
                        capturing the attention of patients, investors, and even celebrities.<br><br>

                        Part of a class of drugs known as GLP-1 agonists, they are given by a weekly injection and work
                        by affecting hunger signals to the brain and slowing the rate at which a person’s stomach
                        empties, making them feel full longer.<br><br>

                        Studies suggest people are likely to have to take the drugs for the rest of their lives to keep
                        the weight off.<br><br>

                        Wegovy is approved for weight-loss in the United States and Europe, while Mounjaro is expected
                        to receive US approval later this year. The enormous demand for the drugs is expected to be
                        worth $100 billion in annual sales within a decade, with as many as 10 different drugs on the
                        market.<br><br>

                        US medical groups are also reviewing their obesity treatment guidelines to consider the best use
                        of Wegovy and similar drugs, with some specialists advocating broad use while others recommend
                        prioritizing them for high-risk patients with health conditions, like diabetes or heart disease,
                        that are exacerbated by excess weight.<br><br>

                        The American Academy of Pediatrics has recommended using such medicines in children age 12 or
                        older with obesity, even though the long-term impacts have yet to be studied.<br><br>

                        The WHO said its revised guidelines will be based on more robust methodology than previous
                        iterations and include up-to-the-minute science. The first draft of the new management
                        guidelines for children and adolescents are expected by the end of this year.<br><br>

                        Branca said the researchers at Mario Negri, as well as other institutions working on the
                        guidelines, had been extensively vetted to avoid conflict of interest concerns.<br><br>

                        Novo Nordisk was suspended earlier this year from the Association of the British Pharmaceutical
                        Industry for its marketing practices, such alleged funding of health professionals and providing
                        training that the association alleged was intended to promote its drug.<br><br>

                        “We really screen the potential conflicts of interest,” said Branca.<br><br>

                        He described obesity as a “rising epidemic.”<br><br>

                        “There are multiple reasons why we really have to take much more serious and bolder action,” he
                        said.
