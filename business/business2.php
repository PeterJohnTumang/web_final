                    <p>
                        The Philippine Stock Exchange pitched a set of amendments to its by-laws, top-billed by
                        increasing quorum when firms decide on a course of action. <br><br>

                        In a disclosure on Thursday, the PSE’s board of directors approved amending Article VI, Secs. 1
                        and 3, and Article III, Sec. 8.<br><br>

                        Disclosure broken down explained that the revision to Article VI, Sec. 1 involved revising a
                        clause on scheduled meetings of the board directors to once a month or as often as deemed
                        necessary.<br><br>

                        The second proposed amendment involved increasing the quorum requirement for business
                        transactions and boards to at least two-thirds as indicated in Article VI, Sec. 3. This would be
                        inline with corporate governance practices recommended for publicly-listed companies, the PSE
                        said.<br><br>

                        The last pitch involved amending Article III, Sec. 8. The proposal would change the title to
                        “Creation of Security Interest over Trading Right,” making it consistent with amendments
                        proposed in their previous meeting back on May 3. <br><br>

                        This marks another round of amendments to this PSE’s by-laws, having proposed changes on remote
                        participation and rules on trading participants.
