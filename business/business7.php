                    <p>
                        Swedish inflation dipped under 10 percent in May, official statistics showed Wednesday, but was
                        still higher than expected with some analysts suggesting superstar Beyonce had tipped the
                        scales. <br><br>

                        Consumer prices rose by 9.7 percent in May year-on-year, down from 10.5 percent in April, the
                        first time inflation came in under 10 percent in over six months.<br><br>

                        "Continued decrease in electricity and food prices contributed to the lower inflation rate in
                        May," Mikael Nordin, statistician at Statistics Sweden, said in a statement.<br><br>

                        At the same time, costs of certain goods and services rose, "for instance hotel and restaurant
                        visits, recreational services, and clothing," the agency said.<br><br>

                        According to Michael Grahn, chief economist for Sweden at Danske Bank, a visit by Beyonce to
                        Stockholm could explain the unexpected rise.<br><br>

                        "Beyonce's start of her world tour in Sweden seems to have coloured May inflation, how much is
                        uncertain," Grahn said in a post to social media.<br><br>

                        Grahn added that her much-hyped concert in May "probably" accounted for 0.2 of the 0.3
                        percentage points added to inflation by hotels and restaurant prices.<br><br>

                        Tens of thousands of fans flocked to Stockholm in the middle of May to catch the two concerts
                        that kicked off her first solo tour in seven years.<br><br>

                        Inflation peaked in December at 12.3 percent -- a more than 30-year high -- then slowed slightly
                        in January to 11.7 percent, but unexpectedly spiked back to 12 percent in February.<br><br>

                        Like its peers in the United States and Europe, Sweden's central bank has repeatedly hiked its
                        guiding rate in an effort to rein in inflation.<br><br>

                        The Riksbank raised the rate to 3.5 percent in late April and said it would "probably" raise it
                        by another quarter-point in June or September.<br><br>

                        Inflation adjusted for fixed interest rates (CPIF) -- the figure used by the Riksbank to guide
                        monetary policy -- was 6.7 percent in May, compared to 7.6 percent in April.<br><br>

                        For 2023 as a whole, the central bank expects the Swedish economy to contract 0.7 percent, and
                        has forecast unadjusted inflation of 8.9 percent and rising unemployment.
