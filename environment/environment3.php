                        Two climate protesters who glued themselves to the base of an ancient sculpture in the Vatican
                        Museums have been ordered to pay damages of 28,000 euros ($30,250), activists said
                        Tuesday.<br><br>

                        The two Italian environmentalists — members of the Last Generation group — were also handed
                        suspended prison sentences for sticking their hands to the base of the Laocoon statue.<br><br>

                        "Judges at the Vatican City State Tribunal sentenced Ester (Goffi) and Guido (Viero) to nine
                        months (suspended) and a fine of 1,620 euros, and ordered them to pay damages of more than
                        28,000 euros," Last Generation said.<br><br>

                        The group said it would appeal.<br><br>

                        The stunt was one of a number of eye-catching acts in recent months designed to focus attention
                        on the urgency of weaning the world off fossil fuels.<br><br>

                        During their protest in August 2022, Goffi and Viero unfurled a banner reading "Last Generation.
                        No gas, no coal" under the marble statue, which was not touched.<br><br>

                        "The Vatican, which is one of the world's last remaining absolute monarchies, proves by this
                        verdict how hypocritical it is," the group said.<br><br>

                        It said the punishment — "for a few drops of glue on a block of marble underneath the Laocoon" —
                        was "out of proportion and absurd", saying the protest was "simply to highlight what the pope
                        has written and advised."<br><br>

                        Pope Francis, who has been outspoken in his commitment to environmental concerns, regularly
                        stresses how crucial it is to preserve the planet for future generations.<br><br>

                        The Ancient Roman statue of Laocoon shows the mythical priest and his sons being crushed to
                        death by giant sea serpents after trying in vain to warn the Trojans of an impending catastrophe
                        — the wooden horse gifted to them by the Greeks.
