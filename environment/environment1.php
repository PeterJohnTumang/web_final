                    <p>
                        The Climate Change Commission (CCC) has expressed optimism that the Commission on Audit's (COA)
                        issuance of new regulatory guidelines would result in the effective implementation of
                        climate-related projects and programs.<br><br>

                        This is after the COA released on April 18 COA Circular 2023-01, which provides guidelines in
                        the audit of programs, activities and projects with Climate Change Expenditure Tagging (CCET) to
                        validate the budget utilization and implementation in accordance with the laws, rules and
                        regulations.<br><br>

                        The recently issued COA Circular No. 2023-01 provides guidelines to national government
                        instrumentalities (NGIs) and COA auditors when auditing programs, activities, and projects
                        (PAPs) part of the Climate Change Expenditure Tagging (CCET) process, under the General
                        Appropriations Act (GAA).<br><br>

                        The COA circular also aims to assess the economy, effectiveness and efficiency of the existing
                        climate change programs.<br><br>

                        CCC vice chairperson and executive director Robert Borje said the new guidelines would pave the
                        way for the proper use of climate funds.<br><br>

                        “The CCC welcomes this audit policy of COA to support the CCET process. The new guidelines
                        ensure not only the efficiency and effectiveness of its implementation, but also the proper
                        utilization of resources intended for climate action,” Borje said in a news release on Thursday.
                        <br><br>

                        “This initiative will go a long way in encouraging transparency and accountability from the
                        government in how climate expenditures are allocated. We will work closely with COA and extend
                        the necessary technical assistance for the conduct of post-implementation assessment of
                        climate-tagged programs in our annual appropriations,” he added.<br><br>

                        Borje said the regulatory guidelines would serve as a tool to promote a low-carbon future and
                        build resilience to climate-related risks and disasters.<br><br>

                        Government programs aimed at building climate resilience and promoting a low-carbon future such
                        as the Enhanced National Greening Program, Flood Mitigation projects, and Early Warning Systems
                        are tagged in the CCET.<br><br>

                        The CCET budget has increased by 64.46 percent from PHP282.36 billion in 2021 to PHP464.50
                        billion in 2023.<br><br>

                        The CCET is a tool used to track and monitor climate-tagged budgets in the annual
                        appropriations.<br><br>

                        The COA Circular emphasizes that all climate-related programs, activities, and projects for 2021
                        onwards should be implemented and delivered.
