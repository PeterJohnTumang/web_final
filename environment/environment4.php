                    <p>
                        Children from 5.6 million low-income families in the Philippines are exposed to the worsening
                        impacts of climate change—a problem they will inherit even though they are least responsible for
                        it.<br><br>

                        This was according to advocacy group Save the Children Philippines, which stressed that climate
                        change will make the existing challenges that children face much worse.<br><br>

                        “Millions of Filipino children remain poor due to inequality and discrimination, and their
                        situation is exacerbated by the climate crisis. Many of them lack access to basic services on
                        health, education, proper nutrition or adequate housing,” Save the Children Philippines CEO
                        Albert Muyot said.<br><br>

                        Families living in poverty and in rural areas have increasingly shouldered the brunt of climate
                        disasters. Poor households also have less support and resources to adapt to climate-related
                        changes.<br><br>

                        According to a 2021 report published by the Save the Children Philippines, Filipino children
                        born in 2020 will experience 4.9 times more scorching heat waves, 2.3 times more river floods,
                        1.2 more droughts, and 1.5 times more crop failures than their grandparents or people born 60
                        years ago.<br><br>

                        “During climate emergencies, many affected families find it hard to send their children to
                        school or even access healthcare. Children suffer the most because they look for work to help
                        augment their daily needs,” Save the Children Philippines said.<br><br>

                        It added that climate change exacerbates violence against women, putting Filipino girls “at
                        greater risk” of being abused, neglected, exploited or subjected to child marriages.<br><br>

                        <b>Climate change at the center</b><br>

                        The advocacy group called for child-centered climate change mitigation and adaptation
                        plans.<br><br>

                        “Without urgent and concrete actions to mitigate the negative impact of climate change and
                        support for the most vulnerable families, we are at a setback in fulfilling children’s right to
                        survive, learn, be protected, and thrive,” said Rexel Abrigo, environmental health advisor of
                        Save the Children Philippines.<br><br>

                        Climate scientists backed by the United Nations earlier called on countries to strengthen
                        measures that will help people cope in a warming world. The Intergovernmental Panel on Climate
                        Change also warned that “rapid and far-reaching transitions” across all sectors and systems are
                        needed to secure a liveable and sustainable future for all.<br><br>
