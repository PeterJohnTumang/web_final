                    <h1 class='l-heading'>Scientist wants center for polymers available across PH</h1>
                    <div class="meta">
                        <small>
                            <i class="fas fa-user"></i>Written by <b>PJNews</b> - May 18, 2023
                        </small>
                        <div class="category category-technology">Technology</div>
                    </div>
                    <p>
                        A Balik Scientist on Thursday expressed his wish that the Center for Sustainable Polymers (CSP)
                        -- currently available for use in Region 10 -- be made available to other regions by making it a
                        national center. <br><br>

                        Balik Scientist Robert Malaluan said he is seeking the government's support on this
                        initiative.<br><br>

                        Located at the Mindanao State University - Iligan State of Technology, the CSP was funded by the
                        Department of Science and Technology (DOST) as a niche center for sustainable polymers across
                        the region.<br><br>

                        "The country has a lot of polymers, and we could make these a high-value product," Malaluan said
                        in a public briefing.<br><br>

                        He cited as an example the CocoFlexSorb developed at the CSP, which seeks to help contain the
                        oil spill in Mindoro. This bio-based polyurethane foam, reportedly has superior oil absorption
                        capacity and can absorb different types of oil from vegetable, kerosene, engine, and bunker
                        oil.<br><br>

                        In the same briefing, CSP program leader Arnold Lubguban said this innovation costs four to five
                        times less than existing petroleum-based absorbents. Further, the coco foam can be reused for
                        about 20 to 30 times.<br><br>

                        Malaluan said they are currently developing other products at the CSP.<br><br>

                        "We are not just studying coconut oil but also other biopolymers, sustainable polymers that we
                        aim to convert into high-value products," he said.<br><br>

                        Lubguban then noted that the country is rich in these natural resources, and these can be
                        sourced from the sea, the trees as well as vegetable oils. "The country is rich in these
                        resources. What we need are more laboratories and researchers to create more products to help
                        our economy," he said.<br><br>

                        He added that he is encouraging other universities to avail of the DOST's program so they could
                        also establish small laboratories.<br><br>

                        The CSP, according to Malaluan, is an advanced laboratory where small and medium enterprises
                        could test their materials. The laboratory has the equipment capable of characterizing certain
                        materials.
