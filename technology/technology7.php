                    <p>
                        The Philippine Council for Industry Energy and Emerging Technology Research and Development
                        (PCIEERD) is urging the government to use a locally-developed air quality monitoring device
                        prior to issuing Environmental Compliance Certificate (ECC) to industries.<br><br>

                        PCIEERD is pushing for standard use of air quality monitoring devices for industries that
                        release high amounts of particulate matter into the air, and is urging the government to
                        consider the Philippine-made Robust Optical Aerosol Monitor (ROAM).<br><br>

                        The air quality devices used as a prerequisite to the issuance of ECC were all imported, PCIEERD
                        Executive Director Enrico Paringit told the Philippine News Agency.<br><br>

                        "Our hope is for ROAM to be rapidly adopted by the LGUs (local government units) and communities
                        in their march towards goals to be smart cities. The quality of air is indicative of a city's
                        sustainability levels and environmental stewardship," he said in an interview on
                        Thursday.<br><br>

                        Paringit added that ROAM could provide access to affordable, reasonable and responsive
                        technology for monitoring and understanding air quality conditions in rapidly urbanizing
                        areas.<br><br>

                        "If the cost is a constraint, the prospective user can go for locally-made, relatively reliable,
                        cost-effective (device)," he said.<br><br>

                        According to PCIEERD, ROAM was developed by researchers from the University of the Philippines -
                        Diliman to monitor air quality and concentration of particulate matter (PM) in the air. It uses
                        laser illumination and a camera to obtain images of particles up to PM2.5 to measure PM
                        concentration in the air.<br><br>

                        The device is also capable of reading temperature and humidity with a five-minute
                        interval.<br><br>

                        PCIEERD said about 10 units of ROAM were fabricated across the country. The device was tested in
                        the municipalities of Agoncillo and Alfonso, Batangas, when the Taal Volcano erupted in
                        2021.<br><br>

                        The Council is urging LGUs to capacitate local industries to produce materials to produce ROAM,
                        and to promote its commercialization.
