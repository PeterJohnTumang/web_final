                    <h1 class='l-heading'>PTRI to establish more bamboo textile fiber innovation hubs </h1>
                    <div class="meta">
                        <small>
                            <i class="fas fa-user"></i>Written by <b>PJNews</b> - May 19, 2023
                        </small>
                        <div class="category category-technology">Technology</div>
                    </div>
                    <p>
                        The Philippine Textile Research Institute (PTRI) will establish at least three more Bamboo
                        Textile Fiber Innovation Hub (BTFIH) until 2024, adding to the current three hubs, its chief
                        said Friday. <br><br>

                        PTRI officer in charge Julius Leaño said one would be launched in Lagangilang, Abra this
                        year.<br><br>

                        In 2024, PTRI will establish a BTFIH in Maramag, Bukidnon and another in Pangasinan, but Leaño
                        said they have yet to determine which town.<br><br>

                        The latest was launched in Maragondon, Cavite on May 3, while the first two are located in
                        Naguilian, La Union and Cauayan, Isabela.<br><br>

                        Leaño said bamboo fibers could be used for clothing and home textile.<br><br>

                        For nonwovens, this could be used for shoes, bags, and acoustic insulation, among
                        others.<br><br>

                        "Bamboo has at least 35 percent textile fiber recovery compared to other sources of fiber, which
                        barely have only 2 percent. It is abundant and robust across the country and it is a sustainable
                        textile fiber source," he told the Philippine News Agency (PNA).<br><br>

                        Having a BTFIH, he said, enables a community to process bamboo into raw bamboo textile fiber
                        (BTF) and eventually make this ready for textile processing.<br><br>

                        "The technologies are simple, deployable, and scalable. The machines can also be fabricated
                        locally for more massive and extensive deployment," Leaño said.<br><br>

                        The raw BTF is priced at about PHP250 per kg., compared to about PHP10 per kg. of bamboo, he
                        said, adding that the BTFIH would enable more material transformation and value
                        addition.<br><br>

                        Meanwhile, according to PTRI, the BTFIH Cavite would ensure that bamboo textile fibers would be
                        available for subsequent processes to produce textiles.<br><br>

                        "The PTRI will use these fibers to spin yarns that will be available for use by the weavers of
                        Maragondon, Cavite," it said.<br><br>

                        The hub was also funded by the Philippine Council for Agriculture, Aquatic and Natural Resources
                        Research and Development through the project, "Field Verification of the Bamboo Textile Material
                        Production and Treatment Technology."
                    </p>
