                    <h1 class='l-heading'>Andrea Brillantes after split from Ricci Rivero: Still healing, but doing okay
                    </h1>
                    <div class="meta">
                        <small>
                            <i class="fas fa-user"></i>Written by <b>PJNews</b> - June 14, 2023
                        </small>
                        <div class="category category-entertainment">Entertainment</div>
                    </div>
                    <p>
                        Breaking her silence on her split with basketball player Ricci Rivero, Andrea Brillantes
                        confessed that she has not fully healed from heartbreak given the fact that she loved someone
                        with all her heart.<br><br>

                        During an interview with Melai Cantiveros, Regine Velasquez, and Jolina Magdangal on the June 14
                        episode of the morning show “Magandang Buhay,” Brillantes said her heart is “okay” despite the
                        breakup.<br><br>

                        “Concerned kaming mga mumshies sa pinagdadaanan mo ngayon, Andrea. Kamusta naman ang puso mo
                        ngayon, malaya na rin ba?” Cantiveros asked the actress. (We mumshies are concerned about what
                        you’re going through right now, Andrea. How is your heart doing, are you feeling free?)<br><br>

                        “‘Yung puso ko, tingin ko, okay naman siya,” she said in response. “I mean, it could be worse sa
                        mga nagaganap ngayon. Pwedeng wala din ako rito today kasi hindi ko kayang humarap sa mga tao.
                        Pero tingin ko naman po sa lahat ng nagaganap ay okay siya ngayon.”<br><br>

                        (I think my heart is doing okay. I mean, it could be worse based on what’s happening now. I
                        could’ve chosen to not show up today because I can’t show myself in front of other people. But I
                        think that despite what’s happening, I’m okay.)<br><br>

                        Without disclosing Rivero’s name, the “Drag You and Me” star said that she has not fully
                        recovered from her heartbreak because she had loved with all her heart, and it might take time
                        for her before she could let go of the pain.<br><br>

                        “Parang hindi pa po ako malaya, fully. Tingin ko po kasi kapag sinabing freedom, merong part din
                        du’n ang acceptance. Kasi na-accept mo na na kailangan mong i-let go ang mga bagay-bagay. Ako po
                        kasi, aminado ako na since ang bago-bago pa lang po talaga ng lahat, at alam mo ‘yon naging
                        totoo kasi sa akin lahat. Ako nagmahal ako nang totoo, hindi ko ‘yon male-let go agad-agad,” she
                        further said.<br><br>

                        (I don’t think I’m fully free. For me, when you say freedom, acceptance plays a part in that.
                        Because you have to accept that you need to let go of things. I’m aware that things are very new
                        right now, and for me those moments were real. I loved someone truly. I can’t let go of that
                        right away.)<br><br>

                        Brillantes also expressed her reluctance to publicly portray herself as “unbothered,” despite
                        her heart telling a different story.<br><br>

                        “Hindi ko masasabing malaya ako nang 100% kasi may mga bagay pang ayaw ko pang pakawalan at may
                        mga bagay pa na pino-process ko pa,” she said. “Kasi ganun naman talaga. Normal lang naman na
                        dapat ay namnamin ko ‘tong mga feeling na ‘to para makapag-heal ako ng maayos. Ayaw ko naman
                        maging pretentious na maki-trend lang na ‘I’m unbothered’ para lang masabi na unbothered [ako].
                        Aminado ako na nakakaramdam ako kasi tao pa rin po ako,” she continued.<br><br>

                        (I can’t say that I’m free 100% because there are things that I can’t let go easily, and there
                        are things that I need to process. Because it is what it is. It’s normal to process these
                        feelings so I can heal properly. I don’t want to be pretentious enough to join the trend of
                        being, “I’m unbothered,” just to say that I’m unbothered. I’m aware of what I’m feeling because
                        I’m only human.)<br><br>

                        Velasquez, meantime, said that Brillantes may currently be in the “mourning stage,” but the
                        latter stressed that she wants to prioritize herself without putting other people down.<br><br>

                        “Pinipili kong maging masaya. Kahit gusto kong magsalita ay pinipili kong huwag na lang para
                        walang gulo. Kahit ang dami kong gustong gawin, pinipili kong piliin na lang ang sarili ko at
                        mag-focus sa kung ano ang meron ako at sa mga taong nagmamahal sa akin,” she added.<br><br>

                        (I’m choosing to be happy. Even if I want to say something, I choose to not do it because I
                        don’t want any more pain. Even if I want to do something, I choose myself and to focus on the
                        things that I have and to those who love me.)<br><br>

                        Rivero confirmed that he and Brillantes called it quits last June 9, although he appealed to the
                        public to respect their decision to keep their split private. Both celebrities have yet to
                        reveal the cause of their break-up.<br><br>

                        Rumors of the basketball player being unfaithful to the actress then circulated on social media,
                        after he shared a photo of himself taken by beauty queen-turned-politician Leren
                        Bautista.<br><br>

                        Days later, Bautista put a stop to the rumors in an official statement on her Facebook page,
                        saying she doesn’t have any romantic involvement with Rivero.<br><br>
                    </p>
