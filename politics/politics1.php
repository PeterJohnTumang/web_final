                    <p>
                        President Ferdinand Marcos Jr. has approved the pilot run of the government’s food stamp
                        program, which will be financed by grants amounting to US$3 million, the Department of Social
                        Welfare and Development (DSWD) said Tuesday.<br><br>

                        The six-month pilot run of “Walang Gutom 2027” will “take place shortly,” DSWD Secretary Rex
                        Gatchalian said in a Palace briefing.<br><br>

                        “The President approved the run of the pilot which is fully funded through grants, grants from
                        the ADB (Asian Development Bank), JICA (Japan International Cooperation Agency) and the French
                        development agency. So that will be $3 million all in all,” according to Gatchalian.<br><br>

                        The DSWD chief said the first 1,000 days of the food stamp program would also seek to address
                        maternal nutrition.<br><br>

                        “The President also wants to bring in pregnant, lactating mothers kasi we have to start looking
                        at stunting in this country and the first 1,000 days program is very important.<br><br>

                        “Alam natin na yung problema ng stunting is very important and very crucial na masgupo natin if
                        we are to invest in human capital. Kaya nga magco-collaborate kami ni (Health) Secretary (Ted)
                        Herbosa dahil meron tayong Philippine Multi-Sectoral Nutrition Program that was launched 60 days
                        ago with the World Bank’s backing,” he added.<br><br>

                        Gatchalian also reiterated that the marching order of Marcos Jr. is to fight against child
                        stunting and hunger.
