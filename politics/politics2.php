                    <p>
                        President Ferdinand Marcos Jr. has assured families affected by the ongoing Mayon Volcano unrest
                        that the national government is always ready to meet their needs while they wait for the
                        volcanic activity to subside.<br><br>

                        “Kami ay nandito upang tiyakin na lahat ng pangangailangan ng mga evacuees. Tinitingnan nga
                        namin na ‘yung response ng national government ay sapat at makausap din kayo, ang mga evacuees,
                        kung ano ‘yung mga inyong pangangailangan,” Marcos Jr. told the evacuees at the Guinobatan
                        Community College evacuation center in Albay province.<br><br>

                        “At huwag kayong mag-aantay nang matagal kung may pangangailangan kayo. Sabihin ninyo sa amin.
                        Nandito si Mayor, nandito si Gov, nandito lahat ng DSWD, nandito ‘yung DILG, nandito silang
                        lahat at nandiyan lang sila para alalayan kayo,” he added.<br><br>

                        The President has directed national government agencies to provide evacuees sheltering at
                        evacuation centers with basic necessities such as food, potable water, medicines, and sanitation
                        kits and equipment.<br><br>

                        The Department of National Defense has been coordinating with all disaster relief response
                        agencies in the country to provide the needs of evacuees, while the Department of Social Welfare
                        and Development is working on extending relief goods, said Marcos Jr.<br><br>

                        “Ang DOH ay sila at saka ang Provincial Health Hospital ninyo, ‘yung municipal hospital niyo
                        tutulong ‘yan at ang ating mga healthcare workers ay sila ang makakapagbigay – sila ang may
                        karapatan na makapagbigay ng gamot para sa inyong lahat,” according to him.

                        The Chief Executive again thanked the United Arab Emirates for providing 50 tons of relief goods
                        after learning of the Mayon’s increasing unrest.<br><br>

                        “At iyon po ‘yung mga idi-distribute natin marami diyan ay galing po sa ating mga kaibigan sa
                        United Arab Emirates. At siguro maybe the reason they are so sympathetic to the Philippines is
                        because they have many Filipinos working in UAE and they feel the closeness and friendship to
                        Filipinos,” said Marcos Jr.<br><br>

                        The Office of Civil Defense reported that 4,400 families or 15,676 individuals were displaced
                        due to Mayon’s restiveness, mostly from the seven municipalities and cities of Albay such as
                        Camalig, Ligao City, Daraga, Guinobatan, Malilipot, Santo Domingo, and Tabaco City.
